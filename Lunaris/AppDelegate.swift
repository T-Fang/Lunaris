    //用于保存后台下载的completionHandler
    var backgroundSessionCompletionHandler: (() -> Void)?
    
    //后台下载完毕后会调用（我们将其交由下载工具类做后续处理）
    func application(_ application: UIApplication,
                     handleEventsForBackgroundURLSession identifier: String,
                     completionHandler: @escaping () -> Void) {
        
        //用于保存后台下载的completionHandler
        backgroundSessionCompletionHandler = completionHandler
        
        //创建download session
        let configuration = URLSessionConfiguration.background(withIdentifier: identifier)
        let downloadssession = URLSession(configuration: configuration, delegate: DownloadManager.shared, delegateQueue: nil)
        
        //指定download session
        DownloadManager.shared.session = downloadssession
    }
    
    
    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        // Override point for customization after application launch.
        UNUserNotificationCenter.current().delegate = self
        return true
    }
    
    //MARK: Notification
    func userNotificationCenter(_ center: UNUserNotificationCenter, didReceive response: UNNotificationResponse, withCompletionHandler completionHandler: @escaping () -> Void) {
